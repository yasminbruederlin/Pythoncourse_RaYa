# Zur Erinnerung:
print('Hallo, Welt')
# printed die Zeichenkette 'Hallo, Welt' in das Terminal

# Definiere Variablen mit gleichzeitiger Wertzuweisung
hello = 'Hallo'
world = 'Welt'
# Ausgabe im Terminal
print(hello, world)

# In Python sind Variablen case-sensitive!
Hello = 'hello'
World = 'world'
# Ausgabe im Terminal
print(hello, world)
print(Hello, World)
